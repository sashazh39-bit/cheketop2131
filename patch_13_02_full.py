def _tj(cids: list[str], kern: str = "-16.66667") -> bytes:
    """CIDs как 4 hex: ['021c','024c',...] -> literal TJ."""
    parts = []
    for cid in cids:
        if len(cid) == 4:
            h, l = int(cid[:2], 16), int(cid[2:], 16)
            parts.append(f"(\\x{h:02x}\\x{l:02x}){kern}".encode())
        elif cid == "028":  # М экранируем
            parts.append(b'(\\x02\\()-16.66667')
    return b" ".join(parts)


